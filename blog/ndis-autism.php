				        <section class="paragraphSection">
				            <h2>What is the NDIS?</h2>
				            <p>NDIS is the abbreviation for ‘National Disability Insurance Scheme’ which is a government-funded initiative designed to provide support for Australians with disabilities, including those with autism spectrum disorder (ASD).</p>
				            <p>The scheme aims to empower individuals with disabilities by providing funding for essential services and support.</p>
				            <p>Unlike previous disability support models, the NDIS operates on an individualised basis, meaning that funding is tailored and allocated based on each person’s specific goals and requirements. For people with autism, this personalised approach ensures they can access services that help with developing life skills, enhancing their independence, and participating more fully in society. </p>
				        </section>

				        <section class="paragraphSection">
				            <h2>Eligibility for NDIS Support</h2>
				            <p>To access NDIS funding, applicants must also meet these conditions:</p>
				            <ul>
				                <li>Under 65 years of age when applying</li>
				                <li>An Australian citizen, permanent resident or with a Protected Special Category Visa</li>
				                <li>Must have a permanent disability that affects their ability to participate in everyday activities</li>
				                <li>Children under 6 years old may be eligible for early intervention support</li>
				            </ul>
				        </section>

				        <section class="paragraphSection">
				            <h2>Autism and the NDIS</h2>
				            <p>Autism is one of the most common disabilities supported by the NDIS. The scheme recognises that autism exists on a spectrum, meaning that individuals with autism may require varying levels of support. The NDIS categorises autism using the DSM-5 (Diagnostic and Statistical Manual of Mental Disorders) criteria and assigns support levels accordingly:</p>
				            <ul>
				                <li><strong>Level 1:</strong> Require some support but can function independently in most cases.</li>
				                <li><strong>Level 2:</strong> Require substantial support in social and daily activities.</li>
				                <li><strong>Level 3:</strong> Individuals require significant support, often needing regular assistance with communication, mobility, and daily tasks.</li>
				            </ul>
				            <p>Generally, those diagnosed with Level 2 or 3 autism are automatically eligible for NDIS support, while those with Level 1 autism may need to provide additional evidence to qualify.</p>
				            <p>Providing supporting documentation such as diagnostic reports from healthcare professionals, therapists or psychologists is crucial for demonstrating NDIS eligibility, particularly for those with Level 1 autism. Therefore, keeping records and evidence is an important step in getting the most from yours or your child’s NDIS plan.</p>
				        </section>

				        <section class="paragraphSection">
				            <h2>Making the Most of an NDIS Plan</h2>
				            <p>Navigating the NDIS can be overwhelming, and autism is not automatically included in the criteria, but there are several strategies that can be used to maximise its benefits:</p>
				            <ul>
				                <li><strong>Plan:</strong> Before your planning meeting, identify specific goals and the types of support needed.</li>
				                <li><strong>Evidence:</strong> Provide detailed reports from medical professionals to strengthen your case for funding.</li>
				                <li><strong>Guidance:</strong> Engage with a Plan Manager or Support Coordinator who can help manage funds, find suitable providers, and ensure services align with NDIS goals.</li>
				                <li><strong>Change:</strong> If a plan does not meet your needs, you can request a review and advocate for additional support, so make changes where necessary.</li>
				            </ul>
				        </section>

