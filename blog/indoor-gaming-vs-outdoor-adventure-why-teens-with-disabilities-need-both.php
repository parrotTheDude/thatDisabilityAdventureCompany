        <!-- ---------- Intro ---------- -->
        <section class="paragraphSection">
          <p>For parents of teenagers, one question comes up again and again: how much screen time is too much? Over the last few decades, gaming has become a huge part of teenage life, and for those with disabilities, it often provides even more of an appeal as a safe and accessible space to learn, connect, and explore. But what happens when time in front of a screen starts to outweigh time outside?</p>

          <p>The truth is, it doesn’t have to be a battle between gaming and outdoor adventure. At TDAC, we believe that both activities bring unique benefits. The key is balance — and recognising that, while both have their benefits, outdoor adventure in particular can have a powerful impact on confidence, health, and social growth, by pushing teens out of their comfort zone.</p>
        </section>

        <!-- ---------- Is Gaming the Enemy? ---------- -->
        <section class="paragraphSection">
          <h2>Is Gaming the Enemy?</h2>
          <p>First, let’s clear something up: gaming itself isn’t a “bad habit.” In fact, it can be incredibly beneficial for teens with disabilities. Some advantages include:</p>

          <ul>
            <li><strong>Cognitive skills:</strong> Many games require problem‑solving, quick decision‑making, and creative thinking, which can help encourage teens to plan, think ahead, and adapt.</li>
            <li><strong>Social connections:</strong> Online gaming allows young people to connect with friends and communities online, sometimes forming friendships that feel just as real as in‑person ones. For teens who struggle with mobility or social anxiety, this can be a lifeline.</li>
            <li><strong>Accessibility:</strong> Unlike many traditional sports or activities, video games are designed to be inclusive. Adaptive controllers and accessible game design mean more teens can participate equally.</li>
          </ul>

          <p>So while parents may worry about “too much gaming,” it’s worth remembering that this is also a space where young people can be learning and growing, particularly if they are living with disabilities.</p>
        </section>

        <!-- ---------- Downsides of Too Much Screen Time ---------- -->
        <section class="paragraphSection">
          <h2>The Downsides of Too Much Screen Time</h2>
          <p>Of course, balance is important. Hours spent sitting in front of a screen can have downsides if it becomes the only activity. Issues that may arise from too much screen time include:</p>

          <ul>
            <li><strong>Physical health risks:</strong> Long periods of sitting can impact posture, weight, and overall fitness.</li>
            <li><strong>Mental wellbeing:</strong> Overexposure to screens and blue light can contribute to overstimulation, poor sleep patterns, or increased anxiety.</li>
            <li><strong>Missed opportunities for social practice:</strong> Online interaction is valuable, but it doesn’t replace the skills gained from face‑to‑face teamwork and communication.</li>
          </ul>

          <p>For parents, we understand this is where the concern usually lies — not in a belief that gaming itself is harmful, but that it can take up all the space for development if other activities aren’t encouraged.</p>
        </section>

