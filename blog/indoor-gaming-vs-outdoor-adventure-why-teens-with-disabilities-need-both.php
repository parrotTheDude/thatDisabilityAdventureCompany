        <!-- ---------- Finding the Right Balance ---------- -->
        <section class="paragraphSection">
          <h2>Finding the Right Balance: Gaming + Adventure</h2>
          <p>The real secret isn’t about choosing one or the other — it’s about combining both. Gaming can fuel imagination and provide a sense of achievement, while outdoor adventures ground teens in the real world and prepare them for life’s challenges.</p>

          <p>Here are some practical tips for parents looking to find that balance:</p>

          <ol>
            <li><strong>Set gentle screen‑time boundaries.</strong> Instead of cutting gaming altogether, set time frames that allow both activities to fit into daily life.</li>
            <li><strong>Make connections between gaming and adventure.</strong> A teen who enjoys strategy games might thrive at orienteering, while one who loves team‑based games might enjoy group hikes or team sports.</li>
            <li><strong>Offer structured opportunities.</strong> Disability‑support organisations, such as TDAC, can provide adventure programmes with safe, inclusive ways for teens to get outside and try new things.</li>
          </ol>

          <p>At TDAC, we tailor our adventures to our participants’ needs. By framing adventure as a complement to gaming—not a replacement—teens are more likely to engage willingly. If you need help planning safe NDIS‑funded outdoor activities for your teen, <a href="https://thatdisabilityadventurecompany.com.au/contact" class="linkUnderline">get in touch today</a>!</p>
        </section>

