            <section class="paragraphSection">
              <h2>4. All-Terrain Wheelchair (Hippocampe or Magic Mobility Extreme X8)</h2>
              <p><strong>Best for:</strong> Rugged terrain, beaches, hiking trails</p>
              <p>Standard mobility devices can struggle on rough paths, sand, or muddy areas. That’s where all-terrain wheelchairs come in. Models like the Hippocampe or Magic Mobility Extreme X8 are built for adventure—with features like fat tyres, suspension, and lightweight frames.</p>
              <p>These chairs open up a world of outdoor opportunities for people with mobility challenges—making nature more accessible and inclusive.</p>
              <h3>Why we love them:</h3>
              <ul>
                <li>Designed for true off-road adventure</li>
                <li>Customisable to suit different needs</li>
                <li>Enables participation in beach, forest, or mountain activities</li>
                <li>Gives participants freedom, not limits</li>
