				      <h2>Why Is This Important?</h2>
				      <p>If you are living with a mild to moderate disability, you might be more likely to experience negative stereotypes, barriers to participation, and social exclusion in your life.</p>
				      <p>This can lead to a lack of confidence which can be a barrier to independent living, and a lack of community and belonging which can cause social isolation.</p>
				    </section>

				    <section class="paragraphSection">
				      <h2>Why Group Activities?</h2>
				      <p>Group activities allow you to create connections and build long-lasting confidence that you can carry with you in daily life, all while having fun!</p>
				      <p>Therefore, group activities can teach you the confidence and daily life skills that are important steps towards your goals, such as independent living, developing social skills, or just enjoying everyday life.</p>
				    </section>

				    <section class="paragraphSection">
				      <h2>Does the NDIS Support Group Activities?</h2>
				      <p>The National Disability Insurance Scheme (NDIS) offers funding for activities that promote social and community participation. This includes meeting others with similar interests and creating community.</p>
				      <p>Therefore, the NDIS supports disability support services, like TDAC, that offer group activities as they are recognised to boost social skills, confidence, and a sense of belonging.</p>
				    </section>

