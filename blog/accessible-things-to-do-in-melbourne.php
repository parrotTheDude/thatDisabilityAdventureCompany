            <section class="paragraphSection">
              <h2>10. Federation Square</h2>
              <p>As Melbourne's cultural hub, Federation Square is home to various events, galleries, and eateries. The square buzzes with a daily program and is the cultural heart of the city. The program is ever-changing, so check out the ‘Fed Square’ website to see what’s on: <a href="https://fedsquare.com/">https://fedsquare.com/</a></p>
              <h3>Accessibility and good-to-knows:</h3>
              <ul>
                <li>The Main Square is made up of sandstone cobblestones, with a marked access path</li>
                <li>Swanston Street offers the best access to the Main Square, and lift access is available to and from The Atrium</li>
                <li>The Big Screen is equipped with a hearing loop and there are accessible toilets located next to Time Out Café</li>
                <li>Quiet spaces and quiet experiences (such as the silent cinema) are available for those seeking a break from the bustling environment</li>
              </ul>
              <p><em>Tip: Although the majority of the space is accessible to wheelchairs, some specific experiences may not have access, check the website for each experience to make sure before you visit</em></p>
