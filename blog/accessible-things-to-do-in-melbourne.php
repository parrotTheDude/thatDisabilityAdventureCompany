            <section class="paragraphSection">
              <p>Melbourne is renowned for its vibrant culture, diverse attractions, and commitment to inclusivity. Whether you're seeking sensory-friendly environments, wheelchair-accessible venues, or simply a fun day out, Melbourne offers a wide range of activities that are both enjoyable and accessible.</p>
            </section>

            <section class="paragraphSection">
              <h2>1. ArtVo</h2>
              <p>ArtVo turns the traditional art exhibition experience upside down with an immersive gallery that is loaded with optical illusions. At ArtVo you can put yourself inside the art, from oceans to outer space. Visitors are encouraged to touch and interact with the art, making it a unique and fun experience for all ages.</p>
              <h3>Accessibility and good-to-knows:</h3>
              <ul>
                <li>The 1400 square-metre space is wheelchair accessible</li>
                <li>There are accessible toilets throughout the precinct, the closest to ArtVo is at the Star Circus</li>
                <li>Free entry for carers with Companion/Carer Cards</li>
                <li>The most convenient accessible parking is on the west side, off Pearl River Road</li>
                <li>An accessible platform super stop is located right outside the shopping centre</li>
                <li>The District’s customer service lounge offers complimentary wheelchair hire</li>
              </ul>
              <p><em>Tip: Tickets are valid for the day so you can go at your own pace and take a break if needed.</em></p>
              <section class="imgContainerArticle">
                <img src="img/blog/artvo.webp" alt="Interactive artwork at ArtVo">
              </section>
            </section>

            <section class="paragraphSection">
              <h2>2. Melbourne Zoo</h2>
              <p>Nestled in the heart of the city, Melbourne Zoo is home to over 320 animal species. It is one of four zoos that make up Zoos Victoria, which is a world-leading zoo-based conservation organisation, dedicated to fighting wildlife extinction.</p>
              <p>Melbourne zoo offers animal talks and experiences all year round and is tailored for people of all ages and abilities.</p>
              <h3>Accessibility and good-to-knows:</h3>
              <ul>
                <li>The zoo boasts wheelchair-accessible pathways, parking, sensory-friendly maps, and quiet zones</li>
                <li>Free entry for all carers who is a holder of a Child Disability Allowance Health Care Card, a Disability Pension Card or a Companion Card</li>
                <li>Quiet zones: The zoo will typically be the quietest between 3:00pm-4:30pm on Monday to Friday.  During these times the zoo aims to minimise high sensory auditory experiences such as vehicle movement, PA announcements and maintenance works</li>
                <li>The zoo offers an Access Key which has all the information you need to plan your visit written in a format that is accessible to all ages and abilities. You can download the PDF from their website at <a href="https://www.zoo.org.au/melbourne/accessibility/">https://www.zoo.org.au/melbourne/accessibility/</a></li>
              </ul>
              <p><em>Tip: To avoid crowds, try attending in Winter, when the zoo may be even quieter and more enjoyable with the potential for more intimate one-on-one experiences with the animals. </em></p>
              <section class="imgContainerArticle">
                <img src="img/blog/melbournezoo.webp" alt="Giraffe at Melbourne Zoo">
              </section>
            </section>

            <section class="paragraphSection">
              <h2>3. ACMI</h2>
              <p>From films to video games, ACMI (originally known as the Australian Centre for the Moving Image) celebrates screen culture past and present. Located at Federation Square in the heart of Melbourne's CBD, ACMI is easy to access and offers the opportunity to explore the world of movies through interactive exhibitions, film screenings, and special events.</p>
              <h3>Accessibility and good-to-knows:</h3>
              <ul>
                <li>There are two entrances, but it's best to use the Flinders Street one</li>
                <li>A series of lifts offer access to all levels of ACMI</li>
                <li>Once inside, everything is accessible, including accessible toilets on two levels</li>
                <li>Complimentary wheelchairs are available</li>
                <li>Carers accompanying companion card holders get free entry to ACMI’s ticketed programs.</li>
                <li>ACMI also has relaxed visits and audio described tours for people with sensory disabilities</li>
              </ul>
              <p><em>Tip: Once a month, ACMI has relaxed visits which involve all exhibitions in the museum being switched to ‘low sensory’ mode between 10 and 11.30am. This is suited for visitors with ASD, sensory sensitivities or anyone who would benefit from a quieter and less busy setting.</em></p>
              <section class="imgContainerArticle">
                <img src="img/blog/acmi.webp" alt="ACMI exhibition view">
              </section>
            </section>

            <section class="paragraphSection">
              <h2>4. National Gallery of Victoria (NGV)</h2>
              <p>NGV is Australia’s oldest and most visited art gallery which offers wheelchair access, sensory-friendly sessions, and Auslan-interpreted tours. Quiet viewing times are also scheduled, catering to visitors who prefer a more subdued environment.</p>
              <h3>Accessibility and good-to-knows:</h3>
              <ul>
                <li>The NGV is free to visit and Companion or Carer Card holders receive concession priced tickets to all exhibitions and programs, with a complimentary ticket for their companion</li>
                <li>The gallery offers Exhibition Access Guides for each exhibition which are available to download from the relevant exhibition page at ngv.vic.gov.au</li>
                <li>For major exhibitions, sensory maps are also available on the NGV website showing the locations of audio-visual content</li>
                <li>Wheelchairs are available for free hire and wheelchair access is available in the auditorium, studios, education theatres and toilets, as well as throughout the gallery spaces</li>
              </ul>
              <section class="imgContainerArticle">
                <img src="img/blog/ngv.webp" alt="National Gallery of Victoria entrance">
              </section>
            </section>

            <section class="paragraphSection">
              <h2>5. Eureka Skydeck</h2>
              <p>Experience panoramic views of Melbourne from the Eureka Skydeck, the Southern Hemisphere's highest observation deck at nearly 300 metres high.</p>
              <p>Alongside the epic view, the Skydeck offers experiences including the VR Voyager Theatre with award-winning 6D chairs that take you through an immersive journey of Melbourne, and the Edge Experience which suspends a transparent glass cube over the edge of the skydeck for more daring visitors!</p>
              <h3>Accessibility and good-to-knows:</h3>
              <ul>
                <li>The Skydeck is wheelchair accessible</li>
                <li>The VR Voyager Theatre and the Edge Experience are also wheelchair accessible</li>
                <li>Carers get free access</li>
                <li>Accessible restrooms and parking are available on-site with discounted parking for Skydeck visitors located directly beneath the building at Wilson Parking Eureka Tower, 70 City Road.</li>
              </ul>
              <section class="imgContainerArticle">
                <img src="img/blog/skydeck.webp" alt="View from Eureka Skydeck">
              </section>
            </section>

            <section class="paragraphSection">
              <h2>6. All Things Equal Café</h2>
              <p>Located in Melbourne's south-east, All Things Equal is a café that champions inclusivity by employing individuals with disabilities to proudly form their ‘all abilities crew’. The cafe champions an employment pathway program that is developed to act as a stepping stone towards mainstream employment, while boasting great food and top tier Melbourne coffee.</p>
              <p>The venue is wheelchair accessible and offers a welcoming atmosphere, making it a great spot to enjoy a meal while supporting a meaningful cause.</p>
              <h3>Accessibility and good-to-knows:</h3>
              <ul>
                <li>The venue is wheelchair accessible</li>
                <li>The cafe has a welcoming atmosphere for all</li>
                <li>The menu is vegetarian and pescatarian</li>
                <li>By visiting you’ll be enjoying a welcoming atmosphere for all abilities while supporting a great cause!</li>
              </ul>
              <section class="imgContainerArticle">
                <img src="img/blog/allthingsequal.webp" alt="All Things Equal Cafe front view">
              </section>
            </section>

            <section class="paragraphSection">
              <h2>7. SEA LIFE Melbourne Aquarium</h2>
              <p>Discover the wonders of marine life at SEA LIFE Melbourne. See some of the world's most incredible animals, including adorable penguins and thousands of tropical fish. The aquarium offers unique experiences such as a private underwater dining experience and a once-in-a-lifetime penguin encounter tour.</p>
              <h3>Accessibility and good-to-knows:</h3>
              <ul>
                <li>The aquarium offers wheelchair access, sensory-friendly maps, and noise-cancelling headphones upon request</li>
                <li>Companion Card holders receive complimentary entry, and assistance animals are welcome</li>
                <li>Quiet periods are during the week (outside of school holidays) after 2pm</li>
                <li>The aquarium offers a Night on the Reef experience which is accessible for all guests, and does not include smoke, haze or strobe lights.</li>
              </ul>
              <section class="imgContainerArticle">
                <img src="img/blog/sealife.webp" alt="Penguins at SEA LIFE Melbourne">
