            <section class="paragraphSection">
              <h2>8. Dialogue in the Dark</h2>
              <p>This immersive exhibition offers a unique experience by guiding visitors through environments in complete darkness, led by blind or low-vision guides. It's an enlightening activity that fosters empathy and understanding, all within a controlled and accessible setting.</p>
              <h3>Accessibility and good-to-knows:</h3>
              <ul>
                <li>Wheelchair accessible with lift access</li>
                <li>In partnership with organisations such as Guide Dogs Australia ensuring support for those with visual impairments</li>
                <li>Alternative experiences - the venue can offer a light version of the dark experience for those who prefer not to be in total darkness</li>
                <li>Reduced pricing for those with valid disability IDs and their companions</li>
              </ul>
              <section class="imgContainerArticle">
                <img src="img/blog/dialogue.webp" alt="Dialogue in the Dark exhibition"></img>
