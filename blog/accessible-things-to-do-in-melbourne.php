            <section class="paragraphSection">
              <h2>9. Royal Botanical Gardens, Melbourne Gardens</h2>
              <p>The Melbourne Gardens were founded in 1846 and extends across 38 hectares in the centre of the city on land that slopes to the river with trees, garden beds, lakes and lawns.</p>
              <p>With free access attracting over 1,900,000 visitors annually, the Melbourne Gardens are a treasured part of the city.</p>
              <h3>Accessibility and good-to-knows:</h3>
              <ul>
                <li>Travellers Aid Companion Service volunteers are available to support people with disability or those with additional mobility requirements travelling to Royal Botanic Gardens Melbourne, from Monday to Friday</li>
                <li>Walkers, motorised and manual wheelchairs are welcome on all guided tours. Most areas of the Melbourne Gardens are accessible by walker and wheelchair</li>
                <li>Accessible toilets and parking are available and can be found on the Melbourne Map on gardens’ website: <a href="http://rbg.vic.gov.au/">http://rbg.vic.gov.au/</a></li>
              </ul>
              <section class="imgContainerArticle">
                <img src="img/blog/royalgardens.webp" alt="Royal Botanical Gardens Melbourne">
