              <p>At TDAC, we live for planning adventures—but we understand that if you or a loved one has a disability, planning can feel a little overwhelming. From finding suitable accommodation to ensuring accessible transport, there are many details to consider.</p>
              <p>But the good news is, with the right planning and support, accessible travel can be safe, enjoyable, and empowering for everyone.</p>
              <p>Whether you’re new to travel or exploring with a disability for the first time, we’ve designed this step-by-step guide to help you confidently plan your next adventure. We’ll cover essential tips on planning, packing, and what to expect, with a special focus on accessible things to do in Melbourne and its surrounding areas in Victoria.</p>
