            <section class="paragraphSection">
              <h2>Step 1: Start with a Plan</h2>
              <p>Before you dive into bookings and itineraries, take time to plan around your specific needs. Ask yourself or your travel companion these key questions:</p>
              <ul>
                <li>What level of mobility support is needed, if any?</li>
                <li>Is specialised transport required?</li>
                <li>Are there specific medical needs or equipment to accommodate?</li>
                <li>Do you need the support of an NDIS disability support company?</li>
              </ul>
              <p>Once you’ve identified your needs, start researching destinations that are known for their accessibility. Victoria offers a growing range of inclusive experiences, from beach matting at popular coastal spots to sensory-friendly museums and national parks with all-abilities trails.</p>
            </section>

            <section class="paragraphSection">
              <h2>Step 2: Research Destinations</h2>
