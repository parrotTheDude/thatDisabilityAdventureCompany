            <section class="paragraphSection">
              <h2>Step 3: Choose the Right Accommodation</h2>
              <p>When booking accommodation, don’t just rely on the “accessible” checkbox on a website. Always call or email to confirm specifics. Ask about:</p>
              <ul>
                <li>Step-free access and lifts</li>
                <li>Doorway widths for wheelchairs</li>
                <li>Roll-in showers or accessible bathrooms</li>
                <li>Proximity to public transport or medical services</li>
                <li>Quiet areas for socialising without over-stimulation</li>
              </ul>
            </section>

            <section class="paragraphSection">
              <h2>Step 4: Pack Smart</h2>
              <p>Packing for accessible travel often includes more than just clothes and toiletries. Consider these extras:</p>
              <ul>
                <li>Medications and prescriptions (plus backups)</li>
                <li>Mobility aids (plus spare batteries or parts)</li>
                <li>Travel documents, including your NDIS plan or emergency contacts</li>
                <li>Comfort items like cushions or noise-cancelling headphones</li>
                <li>A list of local disability support companies near your destination</li>
              </ul>
              <p><em>Pro tip: Pack an “accessibility go-bag” with essentials you might need quickly—such as incontinence products, charging cables, or medical alert info.</em></p>
            </section>

            <section class="paragraphSection">
              <h2>Step 5: Organise Transport</h2>
              <p>Transport is a critical part of accessible travel. If you’re flying, contact your airline in advance to arrange assistance through the terminal and during boarding. Let them know about any mobility devices, oxygen tanks, or other needs.</p>
              <p>For local travel, Victoria has many accessible taxis and rideshare services. Public transport—like trams, trains, and buses in Melbourne—is increasingly equipped with features for people with disabilities. You can also ask your NDIS provider for support coordinating travel.</p>
              <p><em>Pro tip: If you are travelling with medications that need to be kept cool or refrigerated, consider a cool pack such as the Medactiv iCool MediCube—especially if flying.</em></p>
            </section>

            <section class="paragraphSection">
              <h2>Step 6: Know What to Expect</h2>
              <p>Even with the best planning, unexpected hiccups can happen. Being prepared makes all the difference. Here’s what to expect on your accessible adventure:</p>
              <ul>
                <li>Delays or extra time for transitions – build in buffer time</li>
                <li>Helpful locals and staff – don’t be afraid to ask for help</li>
                <li>Moments of joy and empowerment – from sunsets to gallery visits, embrace the adventure</li>
              </ul>
            </section>

