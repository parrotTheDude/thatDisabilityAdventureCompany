              <ul>
                <li>Riviera Winter Wonderland and Ice Skating</li>
                <li>Titanic VR Experience</li>
                <li>Mount Baw Baw</li>
                <li>Chesterfield Farm</li>
              </ul>
              <p>When researching our adventures, we look for locations that clearly state their accessibility features. If you’re planning your own trip, it’s a good idea to read reviews from other travellers with similar needs to get honest feedback.</p>
              <p><em>If you like the look of our recent trips above, get in touch to join our next adventure and we’ll do the planning for you!</em></p>
