          </section>

          <section class="paragraphSection">
            <h3>1. Riviera Winter Festival</h3>
            <p>If you’re craving a coastal winter escape, the Riviera Winter Festival is a must! Running throughout July each year, the foreshore transforms into a magical winter village, complete with an accessible ice rink, light displays, and food stalls showcasing local produce.</p>
            <p>Accessibility is at the heart of the festival, with wheelchair-friendly pathways, accessible amenities, and support services available on-site. Families and support workers can enjoy a relaxed day by the water, with plenty of seating and warm spaces to take a break.</p>
          </section>

          <section class="paragraphSection">
            <h3>2. Mount Baw Baw – Snow Day Trips</h3>
            <p>Winter in Victoria wouldn’t be complete without snow! Mount Baw Baw Alpine Resort has been working to expand its inclusive offerings, from adaptive equipment hire to snow-play zones designed for all ages and abilities.</p>
            <p>With accessible facilities, clear signage, and support available on-site, it’s a very disability-friendly alpine destination. Whether you’re there for tobogganing, a scenic chairlift ride, or simply to soak in the winter scenery, Mount Baw Baw is a fantastic day trip option, and one of our favourites!</p>
          </section>

          <section class="paragraphSection">
            <h3>3. White Night Bendigo – August 2025</h3>
            <p>White Night is a spectacular show of art, light installations, and live music across Melbourne’s CBD. Throughout August, you can wander the streets and watch the city glow with interactive projections and performances.</p>
            <p>Accessibility measures include wheelchair access to viewing areas, Auslan-interpreted performances, and detailed accessibility maps to help you plan your night. White Night’s focus on inclusivity means people of all abilities can experience the wonder of the event.</p>
          </section>

          <section class="paragraphSection">
            <h3>4. Winter Night Market – Queen Victoria Market, Melbourne</h3>
            <p>Melbourne’s iconic Winter Night Market is back for 2025, running on Wednesday evenings through July and August. The night market shines some light on the otherwise dark evenings of Winter. Visitors can enjoy street food from around the world, live music, and performers that roam the crowd.</p>
            <p>The market has worked hard to make the event inclusive: wide aisles for mobility access, accessible bathrooms, and sensory-friendly spaces for those who need a quieter moment. The vibrant atmosphere, combined with warm mulled drinks and comfort food, makes it a great winter night out for everyone!</p>
          </section>

          <section class="paragraphSection">
            <h3>5. National Gallery of Victoria – Winter Exhibitions</h3>
            <p>For those looking to stay warm indoors, the NGV’s winter program is a cultural highlight. This season, expect major international exhibitions alongside community-focused programs.</p>
            <p>The NGV is committed to accessibility, offering wheelchair hire, accessible toilets, ramps, lifts, and hearing loops. They also run relaxed sessions and audio-described tours, ensuring art is truly for everyone.</p>
          </section>
