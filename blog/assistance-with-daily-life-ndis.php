			                    <p>If you are living with mild to moderate disabilities, gaining independence and managing everyday activities can sometimes require extra help. The National Disability Insurance Scheme (NDIS) has a support category called ‘Assistance with Daily Life’ that provides funding for a range of disability support services, including one-to-one support. This is designed to help you live as independently as possible, whether at home or in the community.</p>
