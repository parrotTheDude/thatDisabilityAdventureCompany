			                <section class="paragraphSection">
			                    <h2>What is NDIS Assistance with Daily Life?</h2>
			                    <p>NDIS Assistance with Daily Life is a budget area that falls under NDIS Core Support and covers activities that support daily living, such as:</p>
			                    <ul>
			                        <li><strong>Personal Care:</strong> Assistance with dressing, bathing, grooming, and cleaning.</li>
			                        <li><strong>Household Tasks:</strong> Help with cooking, cleaning, laundry, and gardening.</li>
			                        <li><strong>Meal Preparation:</strong> Maintaining a healthy diet by assisting with grocery shopping and cooking meals.</li>
			                        <li><strong>Transport Assistance:</strong> Getting to medical appointments, social events, or community activities.</li>
			                        <li><strong>Medication Management:</strong> Taking prescribed medications on time.</li>
			                        <li><strong>Skill Development:</strong> Improving the ability to complete daily living tasks independently.</li>
			                    </ul>
			                </section>

			                <section class="paragraphSection">
			                    <h2>What Is Not Covered: Managing Daily Living Expenses</h2>
			                    <p>NDIS support with daily living does not cover general daily living expenses such as rent, food, or entertainment. However, it can be used to fund support services that help you perform daily tasks. For example, while the NDIS won’t pay for your groceries, it can fund a support worker to help you shop and prepare meals.</p>
			                </section>

